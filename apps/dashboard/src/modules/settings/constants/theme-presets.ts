function createThemeValues(seed: PresetSeed): Partial<ThemeCustomization> {
  const fontPair = PRESET_FONT_PAIRS[seed.id] ?? {
    englishFontFamily: DEFAULT_ENGLISH_FONT,
    arabicFontFamily: DEFAULT_ARABIC_FONT,
  };

  return {
    radius: seed.radius,
    cardRadius: seed.cardRadius,
    cardShadow: seed.cardShadow,
    buttonStyle: seed.buttonStyle,
    navbarStyle: seed.navbarStyle,
    navbarHeight: seed.navbarHeight,
    pageBackgroundLight: seed.backgroundLight,
    pageBackgroundDark: seed.backgroundDark,
    backgroundLight: seed.backgroundLight,
    backgroundDark: seed.backgroundDark,
    foregroundLight: seed.foregroundLight,
    foregroundDark: seed.foregroundDark,
    cardLight: seed.cardLight,
    cardDark: seed.cardDark,
    cardForegroundLight: seed.foregroundLight,
    cardForegroundDark: seed.foregroundDark,
    popoverLight: seed.cardLight,
    popoverDark: seed.cardDark,
    popoverForegroundLight: seed.foregroundLight,
    popoverForegroundDark: seed.foregroundDark,
    primaryLight: seed.primaryLight,
    primaryDark: seed.primaryDark,
    primaryForegroundLight: seed.backgroundLight,
    primaryForegroundDark: seed.backgroundDark,
    secondaryLight: seed.secondaryLight,
    secondaryDark: seed.secondaryDark,
    secondaryForegroundLight: seed.foregroundLight,
    secondaryForegroundDark: seed.foregroundDark,
    mutedLight: seed.mutedLight,
    mutedDark: seed.mutedDark,
    mutedForegroundLight: seed.foregroundLight,
    mutedForegroundDark: seed.foregroundDark,
    accentLight: seed.accentLight,
    accentDark: seed.accentDark,
    accentForegroundLight: seed.foregroundLight,
    accentForegroundDark: seed.foregroundDark,
    destructiveLight: seed.destructiveLight,
    destructiveDark: seed.destructiveDark,
    borderLight: seed.borderLight,
    borderDark: seed.borderDark,
    inputLight: seed.borderLight,
    inputDark: seed.borderDark,
    ringLight: seed.ringLight,
    ringDark: seed.ringDark,
    chart1Light: seed.chart1Light,
    chart1Dark: seed.chart1Dark,
    chart2Light: seed.chart2Light,
    chart2Dark: seed.chart2Dark,
    chart3Light: seed.chart3Light,
    chart3Dark: seed.chart3Dark,
    chart4Light: seed.chart4Light,
    chart4Dark: seed.chart4Dark,
    chart5Light: seed.chart5Light,
    chart5Dark: seed.chart5Dark,
    sidebarLight: seed.cardLight,
    sidebarDark: seed.cardDark,
    sidebarForegroundLight: seed.foregroundLight,
    sidebarForegroundDark: seed.foregroundDark,
    sidebarPrimaryLight: seed.primaryLight,
    sidebarPrimaryDark: seed.primaryDark,
    sidebarPrimaryForegroundLight: seed.backgroundLight,
    sidebarPrimaryForegroundDark: seed.backgroundDark,
    sidebarAccentLight: seed.accentLight,
    sidebarAccentDark: seed.accentDark,
    sidebarAccentForegroundLight: seed.foregroundLight,
    sidebarAccentForegroundDark: seed.foregroundDark,
    sidebarBorderLight: seed.borderLight,
    sidebarBorderDark: seed.borderDark,
    sidebarRingLight: seed.ringLight,
    sidebarRingDark: seed.ringDark,
    englishFontFamily: fontPair.englishFontFamily,
    arabicFontFamily: fontPair.arabicFontFamily,
  };
}

